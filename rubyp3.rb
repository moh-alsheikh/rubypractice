
########################################################	RUBY LOOP PRACTICE         #######################################################################

## WHILE LOOP

=begin

print "Please enter how many time should be printed ?  "

counts = gets.chomp
i = counts.to_i

while i > 0

 puts "#{i}  #{Time.now} "
 i -= 1

end

=end 

###################################################################################################################################################################

# INFINITI LOOP ! DANGURES LOOP

=begin

i = 2

while i > 0

	puts "#{Time.now}"  

end

=end

###################################################################################################################################################################

# UNTIL LOOP WORK IF THE CONDITION = FALSE

=begin

i = 1

until i == 20 

  i.times  { puts "*" }

  i += 1
	
end

=end


###################################################################################################################################################################

## FOR LOOP

# include the last number ..
# dont include the last number ... 

=begin

for num in 1..10

  puts num

end

=end


###################################################################################################################################################################

=begin

# LOOP WITH EXIT COMMAND 

start_time = Time.now

while !start_time.nil?

        puts "Time to go =>  #{Time.now - start_time}"

        exit if Time.now - start_time >= 5

end

=end

###################################################################################################################################################################

## DO LOOP

num = 10

loop do

  num -= 2	
  puts num
  break if num == 0

end	














